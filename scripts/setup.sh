#!/usr/bin/env bash
#
# Setup script for the Stage-2 Lean Judge (native / non-Docker).
#
# This script:
#   1. Installs elan (Lean version manager) if not present
#   2. Installs the Lean toolchain pinned by this repo's ``lean-toolchain``
#   3. Runs ``lake update`` + ``lake exe cache get`` to fetch Mathlib oleans
#   4. Builds the judge's own Lean modules (JudgeMagma, JudgeDecide,
#      JudgeFinOp, JudgeSupport)
#   5. Writes ``.env.judge`` and runs a smoke test
#
# This repo is now a self-contained Lean project; it no longer depends on
# cloning ``equational_theories``. Mathlib is the only external Lean dep,
# pulled in by ``lakefile.lean`` via ``lake update``.
#
# Usage:
#   cd <project-root>
#   bash scripts/setup.sh
#
# Environment variables (optional overrides):
#   SKIP_BUILD   — set to 1 to skip ``lake build`` (if oleans already cached)
#   SKIP_DOCKER  — set to 1 to skip the sandbox-image build at the end

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
ROOT="$(cd "$SCRIPT_DIR/.." && pwd)"

info()  { echo "==> $*"; }
warn()  { echo "WARNING: $*" >&2; }
die()   { echo "FATAL: $*" >&2; exit 1; }

# ────────────────────────────────────────────────────
# 1. Check / install elan
# ────────────────────────────────────────────────────
if command -v elan >/dev/null 2>&1; then
    info "elan found: $(elan --version)"
else
    info "Installing elan..."
    curl -sSf https://raw.githubusercontent.com/leanprover/elan/master/elan-init.sh \
        | sh -s -- -y --default-toolchain none
    export PATH="$HOME/.elan/bin:$PATH"
    info "elan installed: $(elan --version)"
fi
export PATH="$HOME/.elan/bin:$PATH"

# ────────────────────────────────────────────────────
# 2. Install the pinned Lean toolchain
# ────────────────────────────────────────────────────
if [ ! -f "$ROOT/lean-toolchain" ]; then
    die "No lean-toolchain found at $ROOT/lean-toolchain"
fi

TOOLCHAIN="$(cat "$ROOT/lean-toolchain" | tr -d '[:space:]')"
info "Required toolchain: $TOOLCHAIN"

# Recent elan (4.x) returns non-zero with "already installed" for an
# existing toolchain. Probe first so setup.sh stays idempotent under
# `set -e`.
if elan toolchain list 2>/dev/null | grep -Fxq "$TOOLCHAIN"; then
    info "Toolchain $TOOLCHAIN already installed."
else
    elan toolchain install "$TOOLCHAIN"
fi
elan default "$TOOLCHAIN"

info "Lean version: $(lean --version)"
info "Lake version: $(lake --version)"

# ────────────────────────────────────────────────────
# 3. Fetch Mathlib + build judge modules
# ────────────────────────────────────────────────────
cd "$ROOT"

if [ "${SKIP_BUILD:-0}" = "1" ]; then
    info "SKIP_BUILD=1, skipping lake update/build"
else
    info "Running lake update (fetches Mathlib)..."
    lake update

    info "Downloading Mathlib cache (this saves hours of compilation)..."
    lake exe cache get || warn "cache get failed, will build from source"

    info "Building judge modules (JudgeMagma, JudgeDecide, JudgeFinOp, JudgeSupport)..."
    lake build JudgeMagma.Magma JudgeDecide.DecideBang JudgeFinOp.MemoFinOp JudgeSupport.Inspect

    info "Lake build complete."
fi

LEAN_BIN="$(which lean)"
LAKE_BIN="$(which lake)"

# ────────────────────────────────────────────────────
# 4. Write env config for the judge
# ────────────────────────────────────────────────────
ENV_FILE="$ROOT/.env.judge"
cat > "$ENV_FILE" <<EOF
# Auto-generated by scripts/setup.sh — source this before running the judge.
# Usage: source .env.judge && python3 scripts/run_harness.py

export LEAN_BIN="$LEAN_BIN"
export LAKE_BIN="$LAKE_BIN"
export PATH="\$HOME/.elan/bin:\$PATH"
EOF

info "Environment config written to $ENV_FILE"

# ────────────────────────────────────────────────────
# 5. Quick smoke test
# ────────────────────────────────────────────────────
info "Running smoke test..."

SMOKE_RESULT=$(python3 -c "
import sys, json
sys.path.insert(0, '.')
from judge.verify import verify_answer, JudgeConfig
config = JudgeConfig(
    lake_bin=__import__('pathlib').Path('$LAKE_BIN'),
    lean_bin=__import__('pathlib').Path('$LEAN_BIN'),
)
problem = json.load(open('tests/fixtures/problems/p_true_basic.json'))
answer = open('tests/fixtures/answers/accepted_true_basic.answer.json').read()
result = verify_answer(problem, answer, config=config)
print(json.dumps({'status': result['status']}, indent=2))
" 2>&1) || true

echo "$SMOKE_RESULT"

if echo "$SMOKE_RESULT" | grep -q '"status": "accepted"'; then
    info "Smoke test PASSED — judge is working."
else
    warn "Smoke test did not return 'accepted'. Check the output above."
    warn "The build may still be incomplete. Re-run: source .env.judge && python3 scripts/run_harness.py"
fi

# ────────────────────────────────────────────────────
# 6. Build solver sandbox image (optional — requires Docker)
# ────────────────────────────────────────────────────
if [ "${SKIP_DOCKER:-0}" = "1" ]; then
    info "SKIP_DOCKER=1, skipping sandbox image build"
elif command -v docker >/dev/null 2>&1 && docker info >/dev/null 2>&1; then
    info "Docker daemon reachable — building solver sandbox image (ee-solver:latest)..."
    if docker build -t ee-solver:latest "$ROOT" >/dev/null; then
        info "Sandbox image built. Enable by setting pipeline/config.json \"sandbox.mode\" = \"docker\"."
    else
        warn "Sandbox image build failed. sandbox.mode=\"docker\" will not work until resolved."
    fi
else
    warn "Docker not available. sandbox.mode=\"docker\" requires Docker Desktop or the docker daemon running."
    warn "Pipeline still works with sandbox.mode=\"none\" (default)."
fi

# ────────────────────────────────────────────────────
# Done
# ────────────────────────────────────────────────────
echo ""
info "Setup complete!"
echo ""
echo "  Next steps (Solo track):"
echo ""
echo "    source .env.judge                       # activate the env every shell"
echo ""
echo "    python3 scripts/run_harness.py          # smoke test the judge (~30s, exits 0 = healthy)"
echo ""
echo "    python3 -m pipeline.runner \\           # run a demo solver end-to-end"
echo "      --submission examples/solo/demos/baseline \\"
echo "      --problems   examples/problems/sample_20.json"
echo ""
echo "  Marathon track:"
echo ""
echo "    python3 scripts/run_marathon.py \\"
echo "      --solver   examples/marathon/demos/baseline \\"
echo "      --manifest tests/marathon_fixtures/manifests/normal_5.jsonl"
echo ""
echo "  See README.md for the full quick start (Docker option, OPENROUTER_API_KEY"
echo "  setup for LLM-using demos, and the per-track tutorials)."
